    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed

        if (txt_input_nama.getText().equals("") || txt_input_jabatan.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Form tidak boleh kosong!");
    } else {
        try {
            // Pastikan menggunakan nama_pegawai (sesuai database Anda)
            String sql = "INSERT INTO pegawai (nama_pegawai, jabatan) VALUES ('" 
                         + txt_input_nama.getText() + "','" 
                         + txt_input_jabatan.getText() + "')";
            Connection conn = Koneksi.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute(); 
            
            JOptionPane.showMessageDialog(null, "Penyimpanan Berhasil");
            load_table();    
            kosongkan_form(); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    }//GEN-LAST:event_btn_tambahActionPerformed

