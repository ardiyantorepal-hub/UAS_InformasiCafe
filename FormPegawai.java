    private void load_table() {
    DefaultTableModel model = new DefaultTableModel();
    // Mengubah Title 1, 2, 3 menjadi nama yang benar di aplikasi
    model.addColumn("ID");
    model.addColumn("Nama Pegawai");
    model.addColumn("Jabatan");
    
    try {
        String sql = "SELECT * FROM pegawai";
        Connection conn = Koneksi.configDB();
        Statement stm = conn.createStatement();
        ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            // Mengambil data sesuai nama kolom di phpMyAdmin
            model.addRow(new Object[]{
                res.getString("id_pegawai"),   // Sesuaikan dengan nama di database
                res.getString("nama_pegawai"), // Sesuaikan dengan nama di database
                res.getString("jabatan")       // Sesuaikan dengan nama di database
            });
        }
        tabel_pegawai.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Load Data: " + e.getMessage());
    }
}

